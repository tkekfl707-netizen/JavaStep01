package HelloBackend;

public class JavaQuest01 {
public static void main(String[] args) {
	System.out.println("Hello Backend Developer!");
	
	int level = 1; //정수값은 따옴표 없이 그대로 표기
	int date = 29;
	
	int sum = level + date; //sum 은 변수(내맘대로 아무렇게나 바꿔 써도 됨 pizza 라고 써도 됨)데이터 담는 상자
							//그니까 sum 이라는 변수 상자 안에 level 이란 데리터와 date 라는 데이터를 더한 것)

	System.out.println(sum); //이거는 콘솔(화면)창에 출력하는 코드
}
}
